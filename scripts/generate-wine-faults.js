/**
 * ONTOLOGY-01E — Generate wine fault pages from wine-fault-catalog.json.
 * Run: node scripts/generate-wine-faults.js
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { loadTaxonomy } from "../lib/taxonomy.js";
import { canonicalUrl, ogUrl, wineFaultUrl } from "../lib/public-url.js";
import { fillTemplate, loadTemplate, SHARED_ROUTES } from "../lib/taxonomy-shell.js";
import { escapeHtml } from "../lib/taxonomy-render.js";
import {
  buildWineFaultGraphEdges,
  buildWineFaultPageContext,
  listWineFaultEntries,
  validateWineFaultCatalog,
} from "../lib/taxonomy-wine-fault.js";
import {
  buildWineFaultJsonLd,
  countWineFaultInternalLinks,
  renderWineFaultBreadcrumb,
  renderWineFaultSections,
} from "../lib/taxonomy-wine-fault-render.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const OUT_DIR = path.join(ROOT, "faults");
const SEARCH_INDEX_OUT = path.join(ROOT, "assets", "js", "wine-fault-search-index.js");
const GRAPH_REPORT_OUT = path.join(ROOT, "reports", "wine-fault-graph-edges.json");
const TEMPLATE = "wine-fault-template.html";

function assemblePage({ template, ctx, bodySectionsHtml, jsonLd, publicRoute }) {
  const jsonLdBlock = jsonLd
    .map((obj) => `<script type="application/ld+json">\n${JSON.stringify(obj, null, 2)}\n</script>`)
    .join("\n  ");

  return fillTemplate(template, {
    PAGE_TITLE: escapeHtml(ctx.title),
    META_DESCRIPTION: escapeHtml(ctx.metaDescription),
    CANONICAL_URL: canonicalUrl(publicRoute),
    OG_URL: ogUrl(publicRoute),
    OG_TITLE: escapeHtml(ctx.title),
    BREADCRUMB: renderWineFaultBreadcrumb(ctx.entity.name),
    BODY_SECTIONS: bodySectionsHtml,
    JSON_LD: jsonLdBlock,
    HOME: SHARED_ROUTES.home,
    PAIRINGS: SHARED_ROUTES.pairings,
    GRAPES: SHARED_ROUTES.grapes,
    SEASONAL: SHARED_ROUTES.seasonal,
    ABOUT: SHARED_ROUTES.about,
    MATRIX: SHARED_ROUTES.matrix,
    PRIVACY: SHARED_ROUTES.privacy,
    TERMS_OF_SERVICE: SHARED_ROUTES.termsOfService,
  });
}

function writeSearchIndex(entities) {
  const entries = entities.map((e) => ({
    slug: e.slug,
    name: e.name,
    entityType: "wine_fault",
    classification: e.classification,
    severity: e.severity,
    href: wineFaultUrl(e.slug),
    haystack: [
      e.slug,
      e.name,
      e.classification,
      e.severity,
      e.cause,
      ...(e.aliases ?? []),
      e.summary,
      ...(e.creates_descriptors ?? []),
      ...(e.reduces_descriptors ?? []),
      ...(e.common_styles ?? []),
      ...(e.common_regions ?? []),
    ]
      .join(" ")
      .toLowerCase(),
  }));

  fs.writeFileSync(
    SEARCH_INDEX_OUT,
    `/**
 * Auto-generated by scripts/generate-wine-faults.js — do not edit.
 * ONTOLOGY-01E wine fault search index (${entries.length} entities).
 */
export const WINE_FAULT_SEARCH_INDEX = ${JSON.stringify(entries, null, 2)};
`,
    "utf8"
  );
}

function renderHub(entities) {
  const byClass = new Map();
  for (const e of entities) {
    const key = e.classification || "Other";
    if (!byClass.has(key)) byClass.set(key, []);
    byClass.get(key).push(e);
  }

  const sections = [...byClass.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([classification, items]) => {
      const links = items
        .sort((a, b) => a.name.localeCompare(b.name))
        .map(
          (e) =>
            `<li><a href="${wineFaultUrl(e.slug)}">${escapeHtml(e.name)}</a> — ${escapeHtml(e.summary.slice(0, 100))}…</li>`
        )
        .join("\n");
      const id = classification.toLowerCase().replace(/[^a-z0-9]+/g, "-");
      return `<section class="term-category-section" aria-labelledby="hub-${id}">
<h2 id="hub-${id}">${escapeHtml(classification)}</h2>
<ul class="term-entity-link-list">${links}</ul>
</section>`;
    })
    .join("\n\n");

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>Wine Faults — Wine Ontology | Pairing Method</title>
  <meta name="description" content="Explore ${entities.length} wine faults — microbial, chemical, physical, and sensory defects linked to descriptors, styles, regions, and winemaking in the knowledge graph.">
  <link rel="canonical" href="https://pairingmethod.com/faults/">
  <link rel="stylesheet" href="/assets/css/styles.css?v=4">
</head>
<body class="wine-fault-hub-page term-entity-page">
  <header>
    <div class="container">
      <a href="/" class="logo" aria-label="Pairing Method home"><img src="/assets/logo.png" alt="Pairing Method" class="logo-img" width="807" height="480" decoding="async"></a>
      <nav aria-label="Main navigation">
        <ul>
          <li><a href="/">Home</a></li>
          <li><a href="/pairings">Pairings</a></li>
          <li><a href="/grapes">Grapes</a></li>
          <li><a href="/seasonal-wine-guides">Seasonal</a></li>
          <li><a href="/about">About</a></li>
        </ul>
      </nav>
    </div>
  </header>
  <main class="container">
    <article class="term-entity">
      <header class="term-entity-hero">
        <p class="term-entity-label">Wine Ontology</p>
        <h1>Wine Faults</h1>
        <p class="wine-fault-summary">Causal defect entities — linked to sensory descriptors, styles, regions, techniques, and serving in the knowledge graph.</p>
      </header>
      ${sections}
    </article>
  </main>
  <footer class="site-footer"><div class="footer-bottom">© Pairing Method</div></footer>
</body>
</html>`;
}

function main() {
  const taxonomy = loadTaxonomy();
  const errors = validateWineFaultCatalog(taxonomy);
  if (errors.length) {
    console.error("Catalog validation failed:");
    for (const e of errors.slice(0, 10)) console.error(`  ✗ ${e}`);
    process.exitCode = 1;
    return;
  }

  const template = loadTemplate(TEMPLATE);
  const entities = listWineFaultEntries();
  let count = 0;
  let linkTotal = 0;
  let edgeTotal = 0;
  const graphReport = [];

  fs.mkdirSync(OUT_DIR, { recursive: true });

  for (const entity of entities) {
    const ctx = buildWineFaultPageContext(entity, { taxonomy });
    const outDir = path.join(OUT_DIR, entity.slug);
    fs.mkdirSync(outDir, { recursive: true });
    const pageUrl = `https://pairingmethod.com${wineFaultUrl(entity.slug)}`;
    const bodySectionsHtml = renderWineFaultSections(taxonomy, ctx);
    const jsonLd = buildWineFaultJsonLd(ctx, pageUrl);
    const html = assemblePage({
      template,
      ctx,
      bodySectionsHtml,
      jsonLd,
      publicRoute: `faults/${entity.slug}/index.html`,
    });

    fs.writeFileSync(path.join(outDir, "index.html"), html, "utf8");
    count += 1;
    linkTotal += countWineFaultInternalLinks(html);
    edgeTotal += ctx.graphEdges.edge_count;
    graphReport.push({
      slug: entity.slug,
      name: entity.name,
      classification: entity.classification,
      edges: ctx.graphEdges,
    });
    console.log(`  ${wineFaultUrl(entity.slug)}`);
  }

  fs.writeFileSync(path.join(OUT_DIR, "index.html"), renderHub(entities), "utf8");
  console.log("  /faults/");

  writeSearchIndex(entities);

  fs.mkdirSync(path.dirname(GRAPH_REPORT_OUT), { recursive: true });
  fs.writeFileSync(
    GRAPH_REPORT_OUT,
    `${JSON.stringify(
      { generated_at: new Date().toISOString(), entity_count: count, total_edges: edgeTotal, entities: graphReport },
      null,
      2
    )}\n`,
    "utf8"
  );

  console.log(`\nWrote ${count} fault pages + hub under ${OUT_DIR}`);
  console.log(`Average internal links per fault page: ${(linkTotal / count).toFixed(1)}`);
  console.log(`Graph edges: ${edgeTotal}`);
  console.log(`Search index → ${path.relative(ROOT, SEARCH_INDEX_OUT)}`);
}

main();
