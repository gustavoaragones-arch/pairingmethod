/**
 * ONTOLOGY-01B — Generate wine region entity pages from wine-region-catalog.json.
 * Run: node scripts/generate-wine-regions.js
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { loadTaxonomy } from "../lib/taxonomy.js";
import { canonicalUrl, ogUrl, wineRegionUrl } from "../lib/public-url.js";
import { fillTemplate, loadTemplate, SHARED_ROUTES } from "../lib/taxonomy-shell.js";
import { escapeHtml } from "../lib/taxonomy-render.js";
import {
  buildRegionReverseIndex,
  buildWineRegionGraphEdges,
  buildWineRegionPageContext,
  listWineRegionEntries,
  validateWineRegionCatalog,
} from "../lib/taxonomy-wine-region.js";
import {
  buildWineRegionJsonLd,
  countWineRegionInternalLinks,
  renderWineRegionBreadcrumb,
  renderWineRegionSections,
} from "../lib/taxonomy-wine-region-render.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const OUT_DIR = path.join(ROOT, "regions");
const SEARCH_INDEX_OUT = path.join(ROOT, "assets", "js", "wine-region-search-index.js");
const GRAPH_REPORT_OUT = path.join(ROOT, "reports", "wine-region-graph-edges.json");
const TEMPLATE = "wine-region-template.html";

function assembleWineRegionPage({ template, ctx, bodySectionsHtml, jsonLd, publicRoute }) {
  const jsonLdBlock = jsonLd
    .map((obj) => `<script type="application/ld+json">\n${JSON.stringify(obj, null, 2)}\n</script>`)
    .join("\n  ");

  return fillTemplate(template, {
    PAGE_TITLE: escapeHtml(ctx.title),
    META_DESCRIPTION: escapeHtml(ctx.metaDescription),
    CANONICAL_URL: canonicalUrl(publicRoute),
    OG_URL: ogUrl(publicRoute),
    OG_TITLE: escapeHtml(ctx.title),
    BREADCRUMB: renderWineRegionBreadcrumb(ctx.region.name),
    BODY_SECTIONS: bodySectionsHtml,
    JSON_LD: jsonLdBlock,
    HOME: SHARED_ROUTES.home,
    PAIRINGS: SHARED_ROUTES.pairings,
    GRAPES: SHARED_ROUTES.grapes,
    SEASONAL: SHARED_ROUTES.seasonal,
    ABOUT: SHARED_ROUTES.about,
    MATRIX: SHARED_ROUTES.matrix,
    PRIVACY: SHARED_ROUTES.privacy,
    TERMS_OF_SERVICE: SHARED_ROUTES.termsOfService,
  });
}

function writeSearchIndex(regions) {
  const entries = regions.map((r) => ({
    slug: r.slug,
    name: r.name,
    entityType: "wine_region",
    country: r.country,
    classification: r.classification,
    href: wineRegionUrl(r.slug),
    haystack: [
      r.slug,
      r.name,
      r.country,
      r.classification,
      ...(r.aliases ?? []),
      r.summary,
      ...(r.subregions ?? []),
    ]
      .join(" ")
      .toLowerCase(),
  }));

  const body = `/**
 * Auto-generated by scripts/generate-wine-regions.js — do not edit.
 * ONTOLOGY-01B wine region search index (${entries.length} regions).
 */
export const WINE_REGION_SEARCH_INDEX = ${JSON.stringify(entries, null, 2)};
`;
  fs.writeFileSync(SEARCH_INDEX_OUT, body, "utf8");
}

function renderRegionsHub(regions) {
  const byCountry = new Map();
  for (const r of regions) {
    const key = r.country || "Other";
    if (!byCountry.has(key)) byCountry.set(key, []);
    byCountry.get(key).push(r);
  }

  const sections = [...byCountry.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([country, items]) => {
      const links = items
        .sort((a, b) => a.name.localeCompare(b.name))
        .map(
          (r) =>
            `<li><a href="${wineRegionUrl(r.slug)}">${escapeHtml(r.name)}</a>${r.classification ? ` <span class="wine-region-hub-class">${escapeHtml(r.classification)}</span>` : ""}</li>`
        )
        .join("\n");
      return `<section class="term-category-section" aria-labelledby="hub-${country.replace(/\s+/g, "-").toLowerCase()}">
<h2 id="hub-${country.replace(/\s+/g, "-").toLowerCase()}">${escapeHtml(country)}</h2>
<ul class="term-entity-link-list">${links}</ul>
</section>`;
    })
    .join("\n\n");

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>Wine Regions — Structured Region Guide | Pairing Method</title>
  <meta name="description" content="Explore ${regions.length} wine regions with climate, grapes, styles, and food pairings — part of the Pairing Method knowledge graph.">
  <link rel="canonical" href="https://pairingmethod.com/regions/">
  <link rel="stylesheet" href="/assets/css/styles.css?v=4">
</head>
<body class="wine-region-hub-page term-entity-page">
  <header>
    <div class="container">
      <a href="/" class="logo" aria-label="Pairing Method home"><img src="/assets/logo.png" alt="Pairing Method" class="logo-img" width="807" height="480" decoding="async"></a>
      <nav aria-label="Main navigation">
        <ul>
          <li><a href="/">Home</a></li>
          <li><a href="/pairings">Pairings</a></li>
          <li><a href="/grapes">Grapes</a></li>
          <li><a href="/seasonal-wine-guides">Seasonal</a></li>
          <li><a href="/about">About</a></li>
        </ul>
      </nav>
    </div>
  </header>
  <main class="container">
    <article class="term-entity">
      <header class="term-entity-hero">
        <p class="term-entity-label">Wine Ontology</p>
        <h1>Wine Regions</h1>
        <p class="wine-region-summary">Geographic backbone of the knowledge graph — connect styles, grapes, descriptors, and pairings across the world's benchmark wine regions.</p>
      </header>
      ${sections}
    </article>
  </main>
  <footer class="site-footer"><div class="footer-bottom">© Pairing Method</div></footer>
</body>
</html>`;
}

function main() {
  const taxonomy = loadTaxonomy();
  const catalogErrors = validateWineRegionCatalog(taxonomy);
  if (catalogErrors.length) {
    console.error("Catalog validation failed:");
    for (const e of catalogErrors.slice(0, 10)) console.error(`  ✗ ${e}`);
    process.exitCode = 1;
    return;
  }

  const template = loadTemplate(TEMPLATE);
  const reverseIndex = buildRegionReverseIndex();
  const regions = listWineRegionEntries();
  let count = 0;
  let linkTotal = 0;
  let edgeTotal = 0;
  let styleLinks = 0;
  let grapeLinks = 0;
  let descriptorLinks = 0;
  const graphReport = [];

  fs.mkdirSync(OUT_DIR, { recursive: true });

  for (const region of regions) {
    const ctx = buildWineRegionPageContext(region, { taxonomy, reverseIndex });
    const outDir = path.join(OUT_DIR, region.slug);
    fs.mkdirSync(outDir, { recursive: true });
    const pageUrl = `https://pairingmethod.com${wineRegionUrl(region.slug)}`;
    const bodySectionsHtml = renderWineRegionSections(taxonomy, ctx);
    const jsonLd = buildWineRegionJsonLd(ctx, pageUrl);
    const html = assembleWineRegionPage({
      template,
      ctx,
      bodySectionsHtml,
      jsonLd,
      publicRoute: `regions/${region.slug}/index.html`,
    });

    fs.writeFileSync(path.join(outDir, "index.html"), html, "utf8");
    count += 1;
    linkTotal += countWineRegionInternalLinks(html);
    edgeTotal += ctx.graphEdges.edge_count;
    styleLinks += ctx.graphEdges.styles.length;
    grapeLinks += ctx.graphEdges.grapes.length;
    descriptorLinks += ctx.graphEdges.descriptors.length;

    graphReport.push({ slug: region.slug, name: region.name, edges: ctx.graphEdges });
    console.log(`  ${wineRegionUrl(region.slug)}`);
  }

  fs.writeFileSync(path.join(OUT_DIR, "index.html"), renderRegionsHub(regions), "utf8");
  console.log("  /regions/");

  writeSearchIndex(regions);

  fs.mkdirSync(path.dirname(GRAPH_REPORT_OUT), { recursive: true });
  fs.writeFileSync(
    GRAPH_REPORT_OUT,
    `${JSON.stringify(
      {
        generated_at: new Date().toISOString(),
        region_count: count,
        total_edges: edgeTotal,
        style_links: styleLinks,
        grape_links: grapeLinks,
        descriptor_links: descriptorLinks,
        regions: graphReport,
      },
      null,
      2
    )}\n`,
    "utf8"
  );

  console.log(`\nWrote ${count} wine region pages + hub under ${OUT_DIR}`);
  console.log(`Average internal links per region page: ${(linkTotal / count).toFixed(1)}`);
  console.log(
    `Graph edges: ${edgeTotal} (styles ${styleLinks}, grapes ${grapeLinks}, descriptors ${descriptorLinks})`
  );
  console.log(`Search index → ${path.relative(ROOT, SEARCH_INDEX_OUT)}`);
}

main();
