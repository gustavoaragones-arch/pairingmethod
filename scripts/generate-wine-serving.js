/**
 * ONTOLOGY-01C — Generate wine serving entity pages from wine-serving-catalog.json.
 * Run: node scripts/generate-wine-serving.js
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { loadTaxonomy } from "../lib/taxonomy.js";
import { canonicalUrl, ogUrl, wineServingUrl } from "../lib/public-url.js";
import { fillTemplate, loadTemplate, SHARED_ROUTES } from "../lib/taxonomy-shell.js";
import { escapeHtml } from "../lib/taxonomy-render.js";
import {
  buildServingReverseIndex,
  buildWineServingGraphEdges,
  buildWineServingPageContext,
  listWineServingEntries,
  validateWineServingCatalog,
} from "../lib/taxonomy-wine-serving.js";
import {
  buildWineServingJsonLd,
  countWineServingInternalLinks,
  renderWineServingBreadcrumb,
  renderWineServingSections,
} from "../lib/taxonomy-wine-serving-render.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const OUT_DIR = path.join(ROOT, "serving");
const SEARCH_INDEX_OUT = path.join(ROOT, "assets", "js", "wine-serving-search-index.js");
const GRAPH_REPORT_OUT = path.join(ROOT, "reports", "wine-serving-graph-edges.json");
const TEMPLATE = "wine-serving-template.html";

const FAMILY_LABELS = {
  temperature: "Serving Temperature",
  glassware: "Glassware",
  decanting: "Decanting",
  cellaring: "Cellaring",
  aging: "Aging Potential",
  mistake: "Serving Mistakes",
};

function assemblePage({ template, ctx, bodySectionsHtml, jsonLd, publicRoute }) {
  const jsonLdBlock = jsonLd
    .map((obj) => `<script type="application/ld+json">\n${JSON.stringify(obj, null, 2)}\n</script>`)
    .join("\n  ");

  return fillTemplate(template, {
    PAGE_TITLE: escapeHtml(ctx.title),
    META_DESCRIPTION: escapeHtml(ctx.metaDescription),
    CANONICAL_URL: canonicalUrl(publicRoute),
    OG_URL: ogUrl(publicRoute),
    OG_TITLE: escapeHtml(ctx.title),
    BREADCRUMB: renderWineServingBreadcrumb(ctx.entity.name),
    BODY_SECTIONS: bodySectionsHtml,
    JSON_LD: jsonLdBlock,
    HOME: SHARED_ROUTES.home,
    PAIRINGS: SHARED_ROUTES.pairings,
    GRAPES: SHARED_ROUTES.grapes,
    SEASONAL: SHARED_ROUTES.seasonal,
    ABOUT: SHARED_ROUTES.about,
    MATRIX: SHARED_ROUTES.matrix,
    PRIVACY: SHARED_ROUTES.privacy,
    TERMS_OF_SERVICE: SHARED_ROUTES.termsOfService,
  });
}

function writeSearchIndex(entities) {
  const entries = entities.map((e) => ({
    slug: e.slug,
    name: e.name,
    entityType: "wine_serving",
    family: e.family,
    familyLabel: FAMILY_LABELS[e.family] ?? e.family,
    href: wineServingUrl(e.slug),
    haystack: [
      e.slug,
      e.name,
      e.family,
      FAMILY_LABELS[e.family] ?? "",
      ...(e.aliases ?? []),
      e.summary,
      ...(e.recommended_for ?? []),
    ]
      .join(" ")
      .toLowerCase(),
  }));

  fs.writeFileSync(
    SEARCH_INDEX_OUT,
    `/**
 * Auto-generated by scripts/generate-wine-serving.js — do not edit.
 * ONTOLOGY-01C wine serving search index (${entries.length} entities).
 */
export const WINE_SERVING_SEARCH_INDEX = ${JSON.stringify(entries, null, 2)};
`,
    "utf8"
  );
}

function renderHub(entities) {
  const byFamily = new Map();
  for (const e of entities) {
    const key = e.family || "other";
    if (!byFamily.has(key)) byFamily.set(key, []);
    byFamily.get(key).push(e);
  }

  const sections = [...byFamily.entries()]
    .map(([family, items]) => {
      const label = FAMILY_LABELS[family] ?? family;
      const links = items
        .sort((a, b) => a.name.localeCompare(b.name))
        .map(
          (e) =>
            `<li><a href="${wineServingUrl(e.slug)}">${escapeHtml(e.name)}</a> — ${escapeHtml(e.summary.slice(0, 100))}…</li>`
        )
        .join("\n");
      return `<section class="term-category-section" aria-labelledby="hub-${family}">
<h2 id="hub-${family}">${escapeHtml(label)}</h2>
<ul class="term-entity-link-list">${links}</ul>
</section>`;
    })
    .join("\n\n");

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>Serving &amp; Service — Wine Ontology | Pairing Method</title>
  <meta name="description" content="Explore ${entities.length} wine serving entities — temperature, glassware, decanting, cellaring, and service mistakes in the Pairing Method knowledge graph.">
  <link rel="canonical" href="https://pairingmethod.com/serving/">
  <link rel="stylesheet" href="/assets/css/styles.css?v=4">
</head>
<body class="wine-serving-hub-page term-entity-page">
  <header>
    <div class="container">
      <a href="/" class="logo" aria-label="Pairing Method home"><img src="/assets/logo.png" alt="Pairing Method" class="logo-img" width="807" height="480" decoding="async"></a>
      <nav aria-label="Main navigation">
        <ul>
          <li><a href="/">Home</a></li>
          <li><a href="/pairings">Pairings</a></li>
          <li><a href="/grapes">Grapes</a></li>
          <li><a href="/seasonal-wine-guides">Seasonal</a></li>
          <li><a href="/about">About</a></li>
        </ul>
      </nav>
    </div>
  </header>
  <main class="container">
    <article class="term-entity">
      <header class="term-entity-hero">
        <p class="term-entity-label">Wine Ontology</p>
        <h1>Serving &amp; Service</h1>
        <p class="wine-serving-summary">The service layer of the knowledge graph — temperature, glassware, decanting, cellaring, and common mistakes linked to styles, grapes, descriptors, and regions.</p>
      </header>
      ${sections}
    </article>
  </main>
  <footer class="site-footer"><div class="footer-bottom">© Pairing Method</div></footer>
</body>
</html>`;
}

function main() {
  const taxonomy = loadTaxonomy();
  const errors = validateWineServingCatalog(taxonomy);
  if (errors.length) {
    console.error("Catalog validation failed:");
    for (const e of errors.slice(0, 10)) console.error(`  ✗ ${e}`);
    process.exitCode = 1;
    return;
  }

  const template = loadTemplate(TEMPLATE);
  const reverseIndex = buildServingReverseIndex();
  const entities = listWineServingEntries();
  let count = 0;
  let linkTotal = 0;
  let edgeTotal = 0;
  const graphReport = [];

  fs.mkdirSync(OUT_DIR, { recursive: true });

  for (const entity of entities) {
    const ctx = buildWineServingPageContext(entity, { taxonomy, reverseIndex });
    const outDir = path.join(OUT_DIR, entity.slug);
    fs.mkdirSync(outDir, { recursive: true });
    const pageUrl = `https://pairingmethod.com${wineServingUrl(entity.slug)}`;
    const bodySectionsHtml = renderWineServingSections(taxonomy, ctx);
    const jsonLd = buildWineServingJsonLd(ctx, pageUrl);
    const html = assemblePage({
      template,
      ctx,
      bodySectionsHtml,
      jsonLd,
      publicRoute: `serving/${entity.slug}/index.html`,
    });

    fs.writeFileSync(path.join(outDir, "index.html"), html, "utf8");
    count += 1;
    linkTotal += countWineServingInternalLinks(html);
    edgeTotal += ctx.graphEdges.edge_count;
    graphReport.push({ slug: entity.slug, name: entity.name, family: entity.family, edges: ctx.graphEdges });
    console.log(`  ${wineServingUrl(entity.slug)}`);
  }

  fs.writeFileSync(path.join(OUT_DIR, "index.html"), renderHub(entities), "utf8");
  console.log("  /serving/");

  writeSearchIndex(entities);

  fs.mkdirSync(path.dirname(GRAPH_REPORT_OUT), { recursive: true });
  fs.writeFileSync(
    GRAPH_REPORT_OUT,
    `${JSON.stringify(
      { generated_at: new Date().toISOString(), entity_count: count, total_edges: edgeTotal, entities: graphReport },
      null,
      2
    )}\n`,
    "utf8"
  );

  console.log(`\nWrote ${count} serving pages + hub under ${OUT_DIR}`);
  console.log(`Average internal links per serving page: ${(linkTotal / count).toFixed(1)}`);
  console.log(`Graph edges: ${edgeTotal}`);
  console.log(`Search index → ${path.relative(ROOT, SEARCH_INDEX_OUT)}`);
}

main();
