/**
 * KNOWLEDGE-03 — Generate descriptor entity pages from wine-taxonomy.json only.
 * Run: node scripts/generate-term-descriptors.js
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { WINES } from "../assets/js/pairing-data.js";
import { buildKnowledgeGraph } from "../lib/taxonomy-graph.js";
import { publicPath, termUrl } from "../lib/public-url.js";
import { loadTaxonomy } from "../lib/taxonomy.js";
import {
  buildDescriptorPageContext,
  buildPairingGuideIndex,
  defaultGlossaryHubHref,
  listDescriptorNodes,
  winesCatalogFromPairingData,
} from "../lib/taxonomy-descriptor.js";
import {
  buildDescriptorJsonLd,
  countInternalLinks,
  renderDescriptorBreadcrumb,
  renderDescriptorSections,
} from "../lib/taxonomy-descriptor-render.js";
import {
  assembleDescriptorPage,
  loadTemplate,
} from "../lib/taxonomy-shell.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const OUT_DIR = path.join(ROOT, "terms");
const SEARCH_INDEX_OUT = path.join(ROOT, "assets", "js", "taxonomy-search-index.js");
const TEMPLATE = "term-descriptor-template.html";

function grapeNamesMap() {
  return Object.fromEntries(WINES.map((w) => [w.id, w.name]));
}

function writeSearchIndex(taxonomy, descriptors) {
  const entries = descriptors.map((node) => {
    const cat = taxonomy.categories.find((c) => c.slug === node.category);
    return {
      slug: node.slug,
      name: node.name,
      category: node.category,
      categoryName: cat?.name ?? node.category,
      href: termUrl(node.slug),
      haystack: [
        node.slug,
        node.name,
        ...(node.search_aliases ?? []),
        cat?.name ?? "",
        node.category,
        node.definition,
      ]
        .join(" ")
        .toLowerCase(),
    };
  });

  const body = `/**
 * Auto-generated by scripts/generate-term-descriptors.js — do not edit.
 * KNOWLEDGE-03 taxonomy search index (${entries.length} descriptors).
 */
export const TAXONOMY_SEARCH_INDEX = ${JSON.stringify(entries, null, 2)};
`;
  fs.writeFileSync(SEARCH_INDEX_OUT, body, "utf8");
}

function main() {
  const taxonomy = loadTaxonomy();
  const template = loadTemplate(TEMPLATE);
  const graph = buildKnowledgeGraph({ taxonomy, WINES, root: ROOT });
  const winesCatalog = winesCatalogFromPairingData(WINES);
  const pairingIndex = buildPairingGuideIndex(ROOT);
  const glossaryHubHref = defaultGlossaryHubHref(taxonomy);
  const grapeNames = grapeNamesMap();
  const descriptors = listDescriptorNodes(taxonomy);

  fs.mkdirSync(OUT_DIR, { recursive: true });

  let count = 0;
  let totalLinks = 0;

  for (const node of descriptors) {
    const ctx = buildDescriptorPageContext(taxonomy, node.slug, {
      winesCatalog,
      pairingIndex,
      glossaryHubHref,
      grapeNames,
      graph,
    });
    if (!ctx) {
      console.warn(`Skip: ${node.slug}`);
      continue;
    }

    const outFile = path.join(OUT_DIR, `${node.slug}.html`);
    const publicRoute = publicPath(`terms/${node.slug}.html`);
    const pageUrl = `https://pairingmethod.com${publicRoute}`;
    const breadcrumbHtml = renderDescriptorBreadcrumb(taxonomy, ctx);
    const bodySectionsHtml = renderDescriptorSections(taxonomy, ctx);
    const jsonLd = buildDescriptorJsonLd(ctx, pageUrl);

    const html = assembleDescriptorPage({
      template,
      ctx,
      breadcrumbHtml,
      bodySectionsHtml,
      jsonLd,
      publicRoute,
    });

    fs.writeFileSync(outFile, html, "utf8");
    totalLinks += countInternalLinks(html);
    count += 1;
  }

  writeSearchIndex(taxonomy, descriptors);

  const avgLinks = count ? (totalLinks / count).toFixed(1) : "0";
  console.log(`Generated ${count} descriptor entity pages → terms/{slug}.html`);
  console.log(`Average internal links per descriptor page: ${avgLinks}`);
  console.log(`Search index → assets/js/taxonomy-search-index.js`);
}

main();
