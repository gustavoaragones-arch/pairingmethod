/**
 * ONTOLOGY-01A — Generate wine style entity pages from wine-style-catalog.json.
 * Run: node scripts/generate-wine-styles.js
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { WINES } from "../assets/js/pairing-data.js";
import { buildKnowledgeGraph } from "../lib/taxonomy-graph.js";
import { loadTaxonomy } from "../lib/taxonomy.js";
import { canonicalUrl, ogUrl, wineStyleUrl } from "../lib/public-url.js";
import { fillTemplate, loadTemplate, SHARED_ROUTES } from "../lib/taxonomy-shell.js";
import { escapeHtml } from "../lib/taxonomy-render.js";
import {
  buildWineStyleGraphEdges,
  buildWineStylePageContext,
  listWineStyleEntries,
  validateWineStyleCatalog,
} from "../lib/taxonomy-wine-style.js";
import {
  buildWineStyleJsonLd,
  countWineStyleInternalLinks,
  renderWineStyleBreadcrumb,
  renderWineStyleSections,
} from "../lib/taxonomy-wine-style-render.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const OUT_DIR = path.join(ROOT, "styles");
const SEARCH_INDEX_OUT = path.join(ROOT, "assets", "js", "wine-style-search-index.js");
const GRAPH_REPORT_OUT = path.join(ROOT, "reports", "wine-style-graph-edges.json");
const TEMPLATE = "wine-style-template.html";

function assembleWineStylePage({ template, ctx, bodySectionsHtml, jsonLd, publicRoute }) {
  const jsonLdBlock = jsonLd
    .map((obj) => `<script type="application/ld+json">\n${JSON.stringify(obj, null, 2)}\n</script>`)
    .join("\n  ");

  return fillTemplate(template, {
    PAGE_TITLE: escapeHtml(ctx.title),
    META_DESCRIPTION: escapeHtml(ctx.metaDescription),
    CANONICAL_URL: canonicalUrl(publicRoute),
    OG_URL: ogUrl(publicRoute),
    OG_TITLE: escapeHtml(ctx.title),
    BREADCRUMB: renderWineStyleBreadcrumb(ctx.style.name),
    BODY_SECTIONS: bodySectionsHtml,
    JSON_LD: jsonLdBlock,
    HOME: SHARED_ROUTES.home,
    PAIRINGS: SHARED_ROUTES.pairings,
    GRAPES: SHARED_ROUTES.grapes,
    SEASONAL: SHARED_ROUTES.seasonal,
    ABOUT: SHARED_ROUTES.about,
    MATRIX: SHARED_ROUTES.matrix,
    PRIVACY: SHARED_ROUTES.privacy,
    TERMS_OF_SERVICE: SHARED_ROUTES.termsOfService,
  });
}

function writeSearchIndex(styles) {
  const entries = styles.map((s) => ({
    slug: s.slug,
    name: s.name,
    entityType: "wine_style",
    classification: s.classification,
    href: wineStyleUrl(s.slug),
    haystack: [
      s.slug,
      s.name,
      ...(s.aliases ?? []),
      s.classification,
      s.summary,
      ...(s.typical_regions ?? []),
      ...(s.primary_grapes ?? []),
    ]
      .join(" ")
      .toLowerCase(),
  }));

  const body = `/**
 * Auto-generated by scripts/generate-wine-styles.js — do not edit.
 * ONTOLOGY-01A wine style search index (${entries.length} styles).
 */
export const WINE_STYLE_SEARCH_INDEX = ${JSON.stringify(entries, null, 2)};
`;
  fs.writeFileSync(SEARCH_INDEX_OUT, body, "utf8");
}

function renderStylesHub(styles) {
  const byClass = new Map();
  for (const s of styles) {
    const key = s.classification || "Other";
    if (!byClass.has(key)) byClass.set(key, []);
    byClass.get(key).push(s);
  }

  const sections = [...byClass.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([classification, items]) => {
      const links = items
        .sort((a, b) => a.name.localeCompare(b.name))
        .map(
          (s) =>
            `<li><a href="${wineStyleUrl(s.slug)}">${escapeHtml(s.name)}</a> — ${escapeHtml(s.summary.slice(0, 120))}…</li>`
        )
        .join("\n");
      return `<section class="term-category-section" aria-labelledby="hub-${classification.replace(/\s+/g, "-").toLowerCase()}">
<h2 id="hub-${classification.replace(/\s+/g, "-").toLowerCase()}">${escapeHtml(classification)}</h2>
<ul class="term-entity-link-list">${links}</ul>
</section>`;
    })
    .join("\n\n");

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>Wine Styles — Structured Style Guide | Pairing Method</title>
  <meta name="description" content="Explore ${styles.length} wine styles with structure profiles, typical descriptors, regions, and food pairings — part of the Pairing Method knowledge graph.">
  <link rel="canonical" href="https://pairingmethod.com/styles/">
  <link rel="stylesheet" href="/assets/css/styles.css?v=4">
</head>
<body class="wine-style-hub-page term-entity-page">
  <header>
    <div class="container">
      <a href="/" class="logo" aria-label="Pairing Method home"><img src="/assets/logo.png" alt="Pairing Method" class="logo-img" width="807" height="480" decoding="async"></a>
      <nav aria-label="Main navigation">
        <ul>
          <li><a href="/">Home</a></li>
          <li><a href="/pairings">Pairings</a></li>
          <li><a href="/grapes">Grapes</a></li>
          <li><a href="/seasonal-wine-guides">Seasonal</a></li>
          <li><a href="/about">About</a></li>
        </ul>
      </nav>
    </div>
  </header>
  <main class="container">
    <article class="term-entity">
      <header class="term-entity-hero">
        <p class="term-entity-label">Wine Ontology</p>
        <h1>Wine Styles</h1>
        <p class="wine-style-summary">First-class style entities in the knowledge graph — structure, descriptors, grapes, and pairings for the world's most searched wine styles.</p>
      </header>
      ${sections}
    </article>
  </main>
  <footer class="site-footer"><div class="footer-bottom">© Pairing Method</div></footer>
</body>
</html>`;
}

function main() {
  const taxonomy = loadTaxonomy();
  const catalogErrors = validateWineStyleCatalog(taxonomy);
  if (catalogErrors.length) {
    console.error("Catalog validation failed:");
    for (const e of catalogErrors) console.error(`  ✗ ${e}`);
    process.exitCode = 1;
    return;
  }

  const template = loadTemplate(TEMPLATE);
  const graph = buildKnowledgeGraph({ taxonomy, WINES, root: ROOT });
  const styles = listWineStyleEntries();
  let count = 0;
  let linkTotal = 0;
  let edgeTotal = 0;
  let descriptorLinks = 0;
  let grapeLinks = 0;
  let pairingLinks = 0;
  const graphReport = [];

  fs.mkdirSync(OUT_DIR, { recursive: true });

  for (const style of styles) {
    const ctx = buildWineStylePageContext(style, { taxonomy, graph, root: ROOT });
    const outDir = path.join(OUT_DIR, style.slug);
    fs.mkdirSync(outDir, { recursive: true });
    const outFile = path.join(outDir, "index.html");
    const publicRoute = wineStyleUrl(style.slug);
    const pageUrl = `https://pairingmethod.com${publicRoute}`;
    const bodySectionsHtml = renderWineStyleSections(taxonomy, ctx);
    const jsonLd = buildWineStyleJsonLd(ctx, pageUrl);
    const html = assembleWineStylePage({
      template,
      ctx,
      bodySectionsHtml,
      jsonLd,
      publicRoute: `styles/${style.slug}/index.html`,
    });

    fs.writeFileSync(outFile, html, "utf8");
    count += 1;
    linkTotal += countWineStyleInternalLinks(html);
    edgeTotal += ctx.graphEdges.edge_count;
    descriptorLinks += ctx.graphEdges.descriptors.length;
    grapeLinks += ctx.graphEdges.grapes.length;
    pairingLinks += ctx.graphEdges.pairings.length;

    graphReport.push({
      slug: style.slug,
      name: style.name,
      edges: ctx.graphEdges,
    });

    console.log(`  ${publicRoute}`);
  }

  fs.writeFileSync(path.join(OUT_DIR, "index.html"), renderStylesHub(styles), "utf8");
  console.log("  /styles/");

  writeSearchIndex(styles);

  fs.mkdirSync(path.dirname(GRAPH_REPORT_OUT), { recursive: true });
  fs.writeFileSync(
    GRAPH_REPORT_OUT,
    `${JSON.stringify(
      {
        generated_at: new Date().toISOString(),
        style_count: count,
        total_edges: edgeTotal,
        descriptor_links: descriptorLinks,
        grape_links: grapeLinks,
        pairing_links: pairingLinks,
        styles: graphReport,
      },
      null,
      2
    )}\n`,
    "utf8"
  );

  console.log(`\nWrote ${count} wine style pages + hub under ${OUT_DIR}`);
  console.log(`Average internal links per style page: ${(linkTotal / count).toFixed(1)}`);
  console.log(`Graph edges: ${edgeTotal} (descriptors ${descriptorLinks}, grapes ${grapeLinks}, pairings ${pairingLinks})`);
  console.log(`Search index → ${path.relative(ROOT, SEARCH_INDEX_OUT)}`);
}

main();
