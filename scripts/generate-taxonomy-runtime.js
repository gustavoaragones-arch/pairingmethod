/**
 * KNOWLEDGE-04 — Generate browser taxonomy runtime + graph reverse index.
 * Run: node scripts/generate-taxonomy-runtime.js
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { WINES, WINE_STYLE_SEMANTICS } from "../assets/js/pairing-data.js";
import {
  buildKnowledgeGraph,
  compactTaxonomyNodeRecord,
  SUPPORTED_NODE_TYPES,
  validateTaxonomySlugs,
} from "../lib/taxonomy-graph.js";
import { loadTaxonomy } from "../lib/taxonomy.js";
import { winesCatalogFromPairingData } from "../lib/taxonomy-descriptor.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const RUNTIME_OUT = path.join(ROOT, "assets", "js", "taxonomy-runtime.js");
const GRAPH_OUT = path.join(ROOT, "assets", "js", "taxonomy-graph-index.js");

function main() {
  const taxonomy = loadTaxonomy();
  const winesCatalog = winesCatalogFromPairingData(WINES);
  const graph = buildKnowledgeGraph({ taxonomy, winesCatalog, WINES, root: ROOT });

  const descriptors = {};
  const linkableNodes = {};
  const phrases = [];

  for (const node of Object.values(taxonomy.nodes)) {
    if (!["descriptor", "group", "category"].includes(node.type)) continue;
    const rec = compactTaxonomyNodeRecord(taxonomy, node.slug);
    if (!rec) continue;
    linkableNodes[node.slug] = rec;
    if (node.type === "descriptor") descriptors[node.slug] = rec;
    const phraseSet = new Set([
      node.name,
      node.slug.replace(/-/g, " "),
      ...(node.search_aliases ?? []),
    ]);
    for (const phrase of phraseSet) {
      if (phrase.length >= 3) {
        phrases.push({ phrase, slug: node.slug, len: phrase.length });
      }
    }
  }
  phrases.sort((a, b) => b.len - a.len);

  const categories = Object.fromEntries(
    taxonomy.categories.map((c) => [
      c.slug,
      { name: c.name, color: c.color, href: `/terms/${c.slug}/` },
    ])
  );

  const semanticSlugs = Object.values(WINE_STYLE_SEMANTICS).flatMap((s) =>
    Object.values(s).flat()
  );
  const missingSemantics = validateTaxonomySlugs(taxonomy, [semanticSlugs]);
  if (missingSemantics.length) {
    console.warn(
      `⚠ ${missingSemantics.length} WINE_STYLE_SEMANTICS slugs missing from taxonomy (e.g. ${missingSemantics.slice(0, 5).join(", ")})`
    );
  }

  const runtimeBody = `/**
 * Auto-generated by scripts/generate-taxonomy-runtime.js — do not edit.
 * KNOWLEDGE-04 browser taxonomy runtime (${Object.keys(descriptors).length} descriptors).
 */
export const TAXONOMY_NODE_TYPES = ${JSON.stringify(SUPPORTED_NODE_TYPES)};

export const TAXONOMY_CATEGORIES = ${JSON.stringify(categories, null, 2)};

export const TAXONOMY_DESCRIPTORS = ${JSON.stringify(descriptors, null, 2)};

export const TAXONOMY_NODES = ${JSON.stringify(linkableNodes, null, 2)};

export const TAXONOMY_PHRASES = ${JSON.stringify(phrases.map(({ phrase, slug }) => ({ phrase, slug })), null, 2)};

export function hasTaxonomyNode(slug) {
  return Object.prototype.hasOwnProperty.call(TAXONOMY_NODES, slug);
}

export function getTaxonomyNode(slug) {
  return TAXONOMY_NODES[slug] ?? null;
}

export function hasDescriptor(slug) {
  return Object.prototype.hasOwnProperty.call(TAXONOMY_DESCRIPTORS, slug);
}

export function getDescriptor(slug) {
  return TAXONOMY_DESCRIPTORS[slug] ?? null;
}

export function taxonomyHref(slug) {
  return getTaxonomyNode(slug)?.href ?? null;
}

export function taxonomyLabel(slug) {
  const n = getTaxonomyNode(slug);
  return n?.name ?? String(slug).replace(/-/g, " ");
}

export function descriptorHref(slug) {
  return taxonomyHref(slug);
}

export function descriptorLabel(slug) {
  return taxonomyLabel(slug);
}
`;

  const graphBody = `/**
 * Auto-generated by scripts/generate-taxonomy-runtime.js — do not edit.
 * KNOWLEDGE-04 reverse relationship index.
 */
export const REVERSE_GRAPH = ${JSON.stringify(
    {
      descriptorToGrapes: graph.reverse.descriptorToGrapes,
      descriptorToPairings: graph.reverse.descriptorToPairings,
      grapeToDescriptors: graph.reverse.grapeToDescriptors,
      grapeToPairings: graph.reverse.grapeToPairings,
      pairingToDescriptors: graph.reverse.pairingToDescriptors,
      pairingToGrapes: graph.reverse.pairingToGrapes,
    },
    null,
    2
  )};

export const GRAPH_STATS = ${JSON.stringify(graph.stats, null, 2)};
`;

  fs.writeFileSync(RUNTIME_OUT, runtimeBody, "utf8");
  fs.writeFileSync(GRAPH_OUT, graphBody, "utf8");

  console.log(`Wrote ${Object.keys(descriptors).length} descriptors → assets/js/taxonomy-runtime.js`);
  console.log(`Wrote reverse graph → assets/js/taxonomy-graph-index.js`);
}

main();
